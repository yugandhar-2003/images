class Gallery extends Component {
  state = {
    image: imagesList[0],
  }

  clickThumbnail = id => {
    this.setState({
      image: imagesList[id],
    })
  }

  render() {
    const {image} = this.state

    return (
      <div className="bg-container">
        <img src={image.imageUrl} alt={image.imageAltText} className="img" />
        <h1 className="title">Nature Photography</h1>
        <p className="content">Nature Photography by Yugandhar</p>
        <ul className="list-container">
          {imagesList.map(eachImage => (
            <ThumbnailItem
              imageDetails={eachImage}
              key={eachImage.id}
              isActive={eachImage.id === image.id}
              changeImage={this.clickThumbnail}
            />
          ))}
        </ul>
      </div>
    )
  }
}

